    private void cmdEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEntrarActionPerformed

        if (txtUserPassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Es necesario ingresar sus credenciales");
        } else {
            if (txtUserPassword.getText().contains(" ")) {
                String[] valor = txtUserPassword.getText().split(" ");
                String usuario = valor[0];
                String contrasenia = valor[1];
                try {
                    if (ControllerLogin.Login(usuario, contrasenia)) {
                        new frmPrincipal().show();
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Usuario  o Contraseña Incorrecta");
                        txtUserPassword.setText("");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "El formato de sus credenciales es invalido");
            }
        }
    }//GEN-LAST:event_cmdEntrarActionPerformed

