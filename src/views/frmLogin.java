    private void cmdLoginAlternoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLoginAlternoActionPerformed
        int resp;
        resp = JOptionPane.showConfirmDialog(null, "Desea enviar el código a su celular  ", "Pregunta", 0);
        if (resp == 0) {
            try {
                String codigo = JOptionPane.showInputDialog("Digite el código recibido: ");
                if (!codigo.isEmpty()) {
                    String[] valor = codigo.split(" ");
                    String usuario = valor[0];
                    String contrasenia = valor[1];
                    if (ControllerLogin.Login(usuario, contrasenia)) {
                        new frmPrincipal().show();
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "El código ingresado es incorrecto");
                    }
                }
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, "Error" + e.toString());
            }
        } else {
            System.exit(0);
        }
    }//GEN-LAST:event_cmdLoginAlternoActionPerformed
